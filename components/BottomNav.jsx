export default function BottomNav() {
  const pathname = usePathname();
  const [navItems, setNavItems] = useState([]);

  useEffect(() => {
    const userStr = localStorage.getItem('user');
    if (!userStr) return;
    try {
      const user = JSON.parse(userStr);
      if (user.role === 'superadmin' || user.role === 'admin') setNavItems(adminNav);
      else if (user.role === 'agent') setNavItems(agentNav);
      else setNavItems(userNav);
    } catch (e) {}
  }, []);

  if (!navItems.length) return null;

  return (
    <nav className="lg:hidden fixed bottom-0 left-0 right-0 z-[200] bg-white border-t border-gray-200 shadow-lg">
      <div className="flex items-center justify-around h-16">
        {navItems.map((item) => {
          const isActive = pathname === item.href || (item.href !== '/admin' && item.href !== '/agent' && item.href !== '/user' && pathname.startsWith(item.href));
          return (
            <Link key={item.href} href={item.href}
              className={`flex flex-col items-center justify-center flex-1 h-full gap-0.5 transition-colors ${
                isActive ? 'text-blue-600' : 'text-gray-400 hover:text-gray-600'
              }`}>
              <div className={`${isActive ? 'text-blue-600' : 'text-gray-400'}`}>
                {item.icon}
              </div>
              <span className={`text-[10px] font-semibold ${isActive ? 'text-blue-600' : 'text-gray-400'}`}>
                {item.label}
              </span>
              {isActive && <div className="absolute bottom-0 w-8 h-0.5 bg-blue-600 rounded-full" />}
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
